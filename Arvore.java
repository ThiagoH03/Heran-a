public class Arvore {
    
    private String folhas;
    private String raizes;
    private int tronco;
    private boolean fruto;

    public String getFolhas() {
        return folhas;
    }
    public void setFolhas(String folhas) {
        this.folhas = folhas;
    }
    public String getRaizes() {
        return raizes;
    }
    public void setRaizes(String raizes) {
        this.raizes = raizes;
    }
    public int getTronco() {
        return tronco;
    }
    public void setTronco(int tronco) {
        this.tronco = tronco;
    }
    public boolean isFruto() {
        return fruto;
    }
    public void setFruto(boolean fruto) {
        this.fruto = fruto;
    }
}
